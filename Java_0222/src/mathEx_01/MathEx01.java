package mathEx_01;

public class MathEx01 {

	public static void main(String[] args) {
		// 수학 클래스
		
		//올림값
		double v1 = Math.ceil(5.3); //6.0
		
		//버림값
		double v6 = Math.floor(5.3); //5.0
		
		//반올림값
		double v7 = Math.round(5.3); //5
		
		//최대값
		int v2 = Math.max(1, 2); //2
		
		//최소값
		int v3 = Math.min(1, 2);
		
		//랜덤값
		double v4 = Math.random();
		System.out.println(v4);
		
		//절대값
		int v5 = Math.abs(-5); //5
		
		
	}

}
